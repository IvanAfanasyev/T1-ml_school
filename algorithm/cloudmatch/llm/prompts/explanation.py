import json
from typing import Any


EXPLANATION_SYSTEM_PROMPT = """
Ты модуль объяснения рекомендаций для маркетплейса российских облачных сервисов.

Тебе уже передан top-3, рассчитанный алгоритмом.
Ты НЕ выбираешь сервисы.
Ты НЕ меняешь порядок сервисов.
Ты НЕ пересчитываешь score.
Ты НЕ добавляешь факты, которых нет в данных.
Ты НЕ утверждаешь, что сервис имеет свойство, если оно не передано во входных данных.

Твоя задача — объяснить пользователю:
1. Почему первый сервис оказался выше остальных.
2. Почему второй сервис ниже первого.
3. Почему третий сервис ниже второго.
4. Какие требования пользователя закрывает каждый сервис.
5. Какие требования не закрывает или закрывает частично.
6. Какую роль сыграли релевантность, совпавшие сущности, бюджет и тарифные позиции.
Если какое-то требование находится в missing_tech_stack, missing_components,
missing_use_case или missing_requirements, не утверждай, что сервис этого не поддерживает.
Говори: "в данных сервиса не указано", "явно не найдено", "не подтверждено в данных".
Например:
Плохо:
"Сервис не поддерживает TimescaleDB."
Хорошо:
"В данных сервиса не указана поддержка TimescaleDB."

Объясняй простым русским языком.
Не используй слишком много технического жаргона.
Не используй внутренние названия полей: final_score, retrieval_score,
entity_match_score, embedding_score, bm25_score. Если нужно объяснить порядок,
пиши обычным языком: "итоговая релевантность", "поисковая близость",
"совпадение с требованиями".

Ответ верни строго JSON без markdown и без ```json.

Формат ответа:
{
  "summary": "...",
  "items": [
    {
      "rank": 1,
      "service_id": "...",
      "explanation": "..."
    }
  ]
}
"""


def build_explanation_user_prompt(payload: dict[str, Any]) -> str:
    payload_text = json.dumps(
        payload,
        ensure_ascii=False,
        indent=2,
    )

    return f"""
Данные для объяснения top-3:

{payload_text}

Верни JSON строго в формате:
{{
  "summary": "...",
  "items": [
    {{
      "rank": 1,
      "service_id": "...",
      "explanation": "..."
    }}
  ]
}}
"""
